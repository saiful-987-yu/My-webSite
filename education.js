
// প্রথমে GSAP-এর সাথে স্ক্রোল অ্যানিমেশনের প্লাগইনটি যুক্ত করে নেওয়া হলো
gsap.registerPlugin(ScrollTrigger);

// বিভিন্ন স্ক্রিন সাইজের জন্য আলাদা মিডিয়া কুয়েরি অবজেক্ট তৈরি করলাম
let mm = gsap.matchMedia();

// স্ক্রিনের চওড়া ১০২৫ পিক্সেল বা তার বেশি হলে (যেমন: ল্যাপটপ ও ডেক্সটপ) এই কোড চলবে
mm.add("(min-width: 1025px)", () => {
  
  // একটি টাইমলাইন তৈরি করলাম যাতে অ্যানিমেশনগুলো একটার পর একটা সিরিয়ালে চলে
  let tl = gsap.timeline({
    scrollTrigger: {
      trigger: "#education", // এই আইডি যুক্ত সেকশনটি স্ক্রিনে আসলে অ্যানিমেশন ট্রিগার হবে
      start: "top 25%",      // সেকশনের মাথাটি স্ক্রিনের ওপর থেকে ২৫% নিচে আসলে শুরু হবে
      end: "+=9000",         // ইউজারকে মোট ৯০০০ পিক্সেল জায়গা পর্যন্ত স্ক্রোল করতে হবে
      pin: true,            // অ্যানিমেশন চলাকালীন পুরো সেকশনটি স্ক্রিনে লক বা আটকে থাকবে
      scrub: 1,             // 'true' এর বদলে '1' দিলে স্ক্রোল করার পর অ্যানিমেশনটি খুব স্মুথলি থামে
      markers: false        // মার্কারটি এখন বন্ধ করে দেওয়া হলো
    }
  });

  // --- কার্ড ১ অ্যানিমেশন ---
  // কার্ড ১ ধীরগতিতে (duration: 3) ডানে সরবে, বড় হবে এবং সবার ওপরে (zIndex: 15) আসবে
  tl.to(".ec1", { x: "25vw", scale: 2, zIndex: 15, duration: 3, ease: "power1.out" })
    // বড় অবস্থায় 0.5 সেকেন্ড ধরে রাখার জন্য
    .to(".ec1", { x: "25vw", scale: 2, zIndex: 15, duration: 0.5, ease: "none" })
    // কার্ড ১ খুব দ্রুত (duration: 0.5) তার আগের জায়গায় ফিরে যাবে এবং নিচে চলে যাবে
    .to(".ec1", { x: 0, scale: 1, zIndex: 5, duration: 0.5, ease: "power1.in" })
    
    // --- র্কাড ২ অ্যানিমেশন ---
    // কার্ড ২ ধীরগতিতে বড় হবে এবং সবার ওপরে (zIndex: 15) চলে আসবে
    .to(".ec2", { x: 0, scale: 2, zIndex: 15, duration: 3, ease: "power1.out" })
    // বড় অবস্থায় ১ সেকেন্ড ধরে রাখার জন্য
    .to(".ec2", { x: 0, scale: 2, zIndex: 15, duration: 1, ease: "none" })
    // কার্ড ২ খুব দ্রুত স্বাভাবিক সাইজে ফিরে যাবে এবং নিচে চলে যাবে
    .to(".ec2", { x: 0, scale: 1, zIndex: 5, duration: 0.5, ease: "power1.in" })
    
    // --- কার্ড ৩ অ্যানিমেশন ---
    // কার্ড ৩ ধীরগতিতে বাঁয়ে সরবে, বড় হবে এবং সবার ওপরে (zIndex: 15) আসবে
    .to(".ec3", { x: "-25vw", scale: 2, zIndex: 15, duration: 3, ease: "power1.out" })
    // বড় অবস্থায় ১ সেকেন্ড ধরে রাখার জন্য
    .to(".ec3", { x: "-25vw", scale: 2, zIndex: 15, duration: 1, ease: "none" })
    // কার্ড ৩ খুব দ্রুত স্বাভাবিক সাইজে ফিরে যাবে এবং নিচে চলে যাবে
    .to(".ec3", { x: 0, scale: 1, zIndex: 5, duration: 1, ease: "power1.in" });

});

// মোবাইল স্ক্রিনের জন্য আলাদা নিয়ম (ম্যাক্স-উইডথ 480px)
mm.add("(max-width: 480px)", () => {
  // মোবাইলে কোনো অ্যানিমেশন হবে না, নরমাল স্ক্রোল হবে
});
